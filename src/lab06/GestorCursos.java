    private void btnEliminarCursoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarCursoMouseClicked
        // Eliminamos 
        if(BuscarIndiceActual() == -1){
            //No hacemos nada
        }
        else{
            //Eliminamos
            listaCursos.remove(BuscarIndiceActual());
            ActualizarModelo();
        }
        
    }//GEN-LAST:event_btnEliminarCursoMouseClicked

    private void btnBuscarCursoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarCursoMouseClicked
        // Boton buscar
        if(txtBuscarCursos.getText().equals("")){
            //NO hacemos nada
        }else{
            //Vamos a buscar el string en la lista actual de Estudiante
            String palabra = txtBuscarCursos.getText();
            int flag = 0;
            for( int i = 0; i< listaCursos.size();i++){
                if(listaCursos.get(i).getCodigo().equals(palabra)){
                    //Mostramos que si esta encontrado
                    flag = 1;
                    //Mostramos mensaje
                    JOptionPane.showMessageDialog(null, ""
                            + "El Curso SI fue encontrado");
                }
            }
            if(flag == 0){
                JOptionPane.showMessageDialog(null, ""
                        + "El Curso NO fue encontrado");
            }
        }
    }//GEN-LAST:event_btnBuscarCursoMouseClicked

   
