    private void lblbtnProfesoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblbtnProfesoresMouseClicked
        //Abrimos la nueva pantalla para que meta los datos de los profesores
        //Solo se abre si esta seleccionado algo del la lista
        if(BuscarIndiceActual() == -1){
            //No hacemos nada
        }else{
            VENT_PROFESORES ven = new VENT_PROFESORES(this);
            ven.CambiarTitulo(
                    listaCursos.get(BuscarIndiceActual()).getNombre());
            ven.setVisible(true);
            ven.setLocationRelativeTo(null);
            this.dispose();
        }
    }//GEN-LAST:event_lblbtnProfesoresMouseClicked

