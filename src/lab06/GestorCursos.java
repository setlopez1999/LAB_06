    public final void ActualizarModelo(){
        this.modeloCursos = new DefaultListModel();
        for (int i = 0; i < listaCursos.size(); i++) {
            String text = listaCursos.get(i).getTEXTO_C();
            this.modeloCursos.addElement(text);
        }
        this.jList4.setModel(modeloCursos);
    }
    
    
